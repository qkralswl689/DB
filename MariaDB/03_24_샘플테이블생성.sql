CREATE TABLE DEPT (
 DEPTNO              INT(2) NOT NULL,
 DNAME               CHAR(14),
 LOC                 CHAR(13),
 CONSTRAINT DEPT_PRIMARY_KEY PRIMARY KEY (DEPTNO));

CREATE TABLE EMP (
 EMPNO               INT(4) NOT NULL,
 ENAME               VARCHAR(10),
 JOB                 VARCHAR(9),
 MGR                 int(4) CONSTRAINT EMP_SELF_KEY REFERENCES EMP (EMPNO),
 HIREDATE            DATE,
 SAL                 INT(7),
 COMM                INT(7),
 DEPTNO              INT(2) NOT NULL,
 CONSTRAINT EMP_FOREIGN_KEY FOREIGN KEY (DEPTNO) REFERENCES DEPT (DEPTNO),
 CONSTRAINT EMP_PRIMARY_KEY PRIMARY KEY (EMPNO));

CREATE TABLE BONUS (
 ENAME               CHAR(10),
 JOB                 CHAR(9),
 SAL                 INT,
 COMM                INT);

CREATE TABLE SALGRADE (
 GRADE               INT,
 LOSAL               INT,
 HISAL               INT);
 
 
